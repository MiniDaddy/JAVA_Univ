package chap03;

public class ArithmeticEx {

	public static void main(String[] args) {
		int a=5, b=2;
		int sum=a+b;
		int mod=a%b;
		int c=++a;
		int d=b++;
		
		System.out.println(sum);
		System.out.println(mod);
		System.out.println(c);
		System.out.println(a);
		System.out.println(d);
		System.out.println(b);

	}

}
